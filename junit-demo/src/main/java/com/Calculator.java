package com;

public class Calculator {

	public int add(int i, int j) {
		
		return i + j;
	}
	public int sub(int i, int j) {
		
		return i - j;
	}
	
	public int mod(int a , int b)
	{
		return a%b;
	}
    
	public int mul(int a , int b)
	{
		return a*b;
	}
	
	public int div(int a ,int b) {
		return a/b;
	}

}
